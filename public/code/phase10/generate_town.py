@dataclass
class BuildingPlacement:
    file: str
    position: list      # [x, y, z]
    rotation_deg: float
    dims: list
    footprint: list     # [x0, y0, x1, y1] axis-aligned bounding box


@dataclass
class RoadSegment:
    x0: float
    y0: float
    x1: float
    y1: float
    width: float
    is_main: bool


