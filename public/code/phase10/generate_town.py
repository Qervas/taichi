def generate_road_grid(town_w, town_h, seed=42):
    """Generate road grid dividing town into blocks.

    Returns list of RoadSegments and list of block AABBs.
    """
    rng = np.random.default_rng(seed)

    # Generate N-S road X positions (vertical roads)
    ns_positions = []
    x = 0.0
    ns_positions.append((x, True))  # left edge, main road
    x += MAIN_ROAD_W

    while x < town_w - 30:
        block_w = rng.uniform(70, 110)
        x += block_w
        is_main = len(ns_positions) % 3 == 0
        road_w = MAIN_ROAD_W if is_main else SIDE_ROAD_W
        ns_positions.append((x, is_main))
        x += road_w

    # Right edge
    ns_positions.append((town_w - MAIN_ROAD_W, True))

    # Generate E-W road Y positions (horizontal roads)
    ew_positions = []
    y = 0.0
    ew_positions.append((y, True))  # bottom edge
    y += MAIN_ROAD_W

    while y < town_h - 30:
        block_h = rng.uniform(60, 90)
        y += block_h
        is_main = len(ew_positions) % 3 == 0
        road_w = MAIN_ROAD_W if is_main else SIDE_ROAD_W
        ew_positions.append((y, is_main))
        y += road_w

    ew_positions.append((town_h - MAIN_ROAD_W, True))

    # Create road segments
    roads = []

    # N-S roads (vertical, full height)
    for rx, is_main in ns_positions:
        w = MAIN_ROAD_W if is_main else SIDE_ROAD_W
        roads.append(RoadSegment(rx, 0, rx + w, town_h, w, is_main))

    # E-W roads (horizontal, full width)
    for ry, is_main in ew_positions:
        w = MAIN_ROAD_W if is_main else SIDE_ROAD_W
        roads.append(RoadSegment(0, ry, town_w, ry + w, w, is_main))

    # Extract blocks (spaces between roads)
    blocks = []
    # Sort road edges
    ns_edges = sorted([(rx, MAIN_ROAD_W if m else SIDE_ROAD_W)
                       for rx, m in ns_positions])
    ew_edges = sorted([(ry, MAIN_ROAD_W if m else SIDE_ROAD_W)
                       for ry, m in ew_positions])

    for i in range(len(ns_edges) - 1):
        x0 = ns_edges[i][0] + ns_edges[i][1]  # right edge of left road
        x1 = ns_edges[i + 1][0]                # left edge of right road
        if x1 - x0 < 20:
            continue
        for j in range(len(ew_edges) - 1):
            y0 = ew_edges[j][0] + ew_edges[j][1]
            y1 = ew_edges[j + 1][0]
            if y1 - y0 < 20:
                continue
            blocks.append([x0, y0, x1, y1])

    return roads, blocks


def place_buildings(blocks, buildings, seed=42):
    """Place buildings within blocks."""
    rng = np.random.default_rng(seed + 1)
    placements = []
    bi = 0  # building index

    for block in blocks:
        bx0, by0, bx1, by1 = block
        block_w = bx1 - bx0
        block_h = by1 - by0

        # Inset by sidewalk + setback
        inset = SIDEWALK_W + BUILDING_SETBACK
        inner_w = block_w - 2 * inset
        inner_h = block_h - 2 * inset

        if inner_w < 15 or inner_h < 10:
            continue

        # Try to fit buildings in a grid within the block
        placed_in_block = []
        cursor_y = by0 + inset
        max_per_block = 6
        attempts = 0

        while cursor_y < by1 - inset - 10 and len(placed_in_block) < max_per_block:
            if bi >= len(buildings):
                break

            bldg = buildings[bi % len(buildings)]
            bw, bd, bh = bldg.dims

            # Try both orientations — prefer the one that fits width
            fits_normal = (bw <= inner_w and bd + cursor_y - by0 - inset <= inner_h)
            fits_rotated = (bd <= inner_w and bw + cursor_y - by0 - inset <= inner_h)

            if not fits_normal and not fits_rotated:
                bi += 1
                attempts += 1
                if attempts > 10:
                    break
                continue

            if fits_normal and fits_rotated:
                rotate = rng.random() < 0.3
            elif fits_rotated:
                rotate = True
            else:
                rotate = False

            rot_deg = 90.0 if rotate else 0.0
            eff_w = bd if rotate else bw
            eff_d = bw if rotate else bd

            cx = bx0 + inset + inner_w / 2 + rng.uniform(-3, 3)
            cy = cursor_y + eff_d / 2

            if cy + eff_d / 2 > by1 - inset:
                bi += 1
                attempts += 1
                if attempts > 10:
                    break
                continue

            fp = [cx - eff_w / 2, cy - eff_d / 2,
                  cx + eff_w / 2, cy + eff_d / 2]

            p = BuildingPlacement(
                file=bldg.file,
                position=[cx, cy, 0.0],
                rotation_deg=rot_deg,
                dims=bldg.dims,
                footprint=fp,
            )
            placements.append(p)
            placed_in_block.append(p)
            bi += 1
            attempts = 0

            # Advance cursor
            cursor_y = cy + eff_d / 2 + 8.0  # 8m gap between buildings

    return placements


