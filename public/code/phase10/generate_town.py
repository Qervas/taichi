# Town layout
TOWN_W = 500.0       # X extent (meters)
TOWN_H = 400.0       # Y extent (meters)
MAIN_ROAD_W = 14.0   # Main road width
SIDE_ROAD_W = 8.0    # Secondary road width
CURB_H = 0.15        # Curb height
CURB_W = 0.20        # Curb width
SIDEWALK_W = 3.0     # Sidewalk width
BUILDING_SETBACK = 2.0  # Gap between sidewalk and building
TREE_SPACING = 15.0   # Trees every N meters along streets
TREE_TRUNK_R = 0.25
TREE_TRUNK_H = 3.5
TREE_CROWN_R = 2.5
TREE_CROWN_H = 4.5
TREE_SIDES = 8        # Polygon sides for tree cylinder/cone


