def load_catalog(models_dir, catalog_path):
    """Load building catalog from JSON (generated by scan_buildings.py)."""
    if os.path.exists(catalog_path):
        with open(catalog_path) as f:
            data = json.load(f)
        return [BuildingInfo(**d) for d in data]
    else:
        raise FileNotFoundError(
            f"Building catalog not found: {catalog_path}\n"
            f"Run scan_buildings.py first.")


def select_buildings(catalog, n, seed=42):
    """Select N diverse buildings from catalog."""
    rng = np.random.default_rng(seed)

    # Sort by height for diversity
    by_height = sorted(catalog, key=lambda b: b.dims[2])

    # Stratified sampling: pick evenly from height distribution
    indices = np.linspace(0, len(by_height) - 1, min(n, len(by_height)),
                          dtype=int)
    # Add some randomness
    selected = [by_height[i] for i in indices]

    # If we need more, cycle through catalog with shuffling
    full_catalog = list(catalog)
    rng.shuffle(full_catalog)
    idx = 0
    while len(selected) < n:
        selected.append(full_catalog[idx % len(full_catalog)])
        idx += 1

    rng.shuffle(selected)
    return selected[:n]


