def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--output", default="./town_export")
    parser.add_argument("--n_buildings", type=int, default=45)
    parser.add_argument("--seed", type=int, default=42)
    parser.add_argument("--catalog", default=CATALOG_PATH)
    args = parser.parse_args()

    t0 = time.time()
    print("=" * 60)
    print("Phase 10 — Town Generator")
    print("=" * 60)

    os.makedirs(args.output, exist_ok=True)

    # Load building catalog
    print("\n[1/6] Loading building catalog...")
    catalog = load_catalog(MODELS_DIR, args.catalog)
    print(f"  {len(catalog)} buildings available")

    # Select buildings
    print(f"\n[2/6] Selecting {args.n_buildings} buildings...")
    selected = select_buildings(catalog, args.n_buildings, args.seed)
    total_tris = sum(b.n_faces for b in selected)
    print(f"  Selected {len(selected)} buildings, {total_tris:,} tris")

    # Generate road grid
    print("\n[3/6] Generating road grid...")
    roads, blocks = generate_road_grid(TOWN_W, TOWN_H, args.seed)
    print(f"  {len(roads)} road segments, {len(blocks)} blocks")

    # Place buildings
    print("\n[4/6] Placing buildings...")
    placements = place_buildings(blocks, selected, args.seed)
    footprints = [p.footprint for p in placements]
    print(f"  {len(placements)} buildings placed")

    # Generate infrastructure geometry
    print("\n[5/6] Generating infrastructure...")

    road_v, road_f = generate_road_meshes(roads)
    print(f"  Roads: {len(road_v)} verts, {len(road_f)} faces")

    curb_v, curb_f = generate_curb_meshes(roads)
    print(f"  Curbs: {len(curb_v)} verts, {len(curb_f)} faces")

    sw_v, sw_f = generate_sidewalk_meshes(roads)
    print(f"  Sidewalks: {len(sw_v)} verts, {len(sw_f)} faces")

    tree_v, tree_f = generate_trees(roads, footprints, TOWN_W, TOWN_H,
                                     args.seed)
    n_trees = len(tree_f) // 20 if len(tree_f) > 0 else 0  # rough estimate
    print(f"  Trees: ~{n_trees} trees, {len(tree_v)} verts")

    crosswalk_v, crosswalk_f = generate_crosswalks(roads)
    print(f"  Crosswalks: {len(crosswalk_v)} verts, {len(crosswalk_f)} faces")

    centerline_v, centerline_f = generate_center_lines(roads)
    print(f"  Center lines: {len(centerline_v)} verts, {len(centerline_f)} faces")

    ground_v, ground_f = generate_ground(TOWN_W, TOWN_H)

    # Export
    print("\n[6/6] Exporting...")

    save_obj(os.path.join(args.output, "roads.obj"), road_v, road_f, "roads")
    save_obj(os.path.join(args.output, "curbs.obj"), curb_v, curb_f, "curbs")
    save_obj(os.path.join(args.output, "sidewalks.obj"), sw_v, sw_f,
             "sidewalks")
    save_obj(os.path.join(args.output, "trees.obj"), tree_v, tree_f, "trees")
    save_obj(os.path.join(args.output, "ground.obj"), ground_v, ground_f,
             "ground")
    if len(crosswalk_v) > 0:
        save_obj(os.path.join(args.output, "crosswalks.obj"),
                 crosswalk_v, crosswalk_f, "crosswalks")
    if len(centerline_v) > 0:
        save_obj(os.path.join(args.output, "centerlines.obj"),
                 centerline_v, centerline_f, "centerlines")

    # Scene descriptor
    scene = {
        "town_size": [TOWN_W, TOWN_H],
        "seed": args.seed,
        "buildings": [asdict(p) for p in placements],
        "road_segments": [asdict(r) for r in roads],
        "blocks": blocks,
        "infrastructure": {
            "roads_obj": "roads.obj",
            "curbs_obj": "curbs.obj",
            "sidewalks_obj": "sidewalks.obj",
            "trees_obj": "trees.obj",
            "ground_obj": "ground.obj",
            "crosswalks_obj": "crosswalks.obj",
            "centerlines_obj": "centerlines.obj",
        },
        "stats": {
            "n_buildings": len(placements),
            "n_trees": n_trees,
            "n_road_segments": len(roads),
            "n_blocks": len(blocks),
            "building_tris": total_tris,
        },
    }

    scene_path = os.path.join(args.output, "scene.json")
    with open(scene_path, 'w') as f:
        json.dump(scene, f, indent=2)

    elapsed = time.time() - t0
    print(f"\n{'=' * 60}")
    print(f"Town generated in {elapsed:.2f}s")
    print(f"  Buildings: {len(placements)}")
    print(f"  Trees: {n_trees}")
    print(f"  Roads: {len(roads)} segments")
    print(f"  Output: {args.output}/")
    print(f"  Scene: {scene_path}")
    print(f"{'=' * 60}")


if __name__ == "__main__":
    main()
