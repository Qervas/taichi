def generate_crosswalks(roads):
    """Generate zebra crossing markings at intersections."""
    all_v, all_f = [], []
    offset = 0
    z = 0.04  # above road surface

    # Separate NS and EW roads
    ns_roads = [r for r in roads if (r.y1 - r.y0) > (r.x1 - r.x0)]
    ew_roads = [r for r in roads if (r.x1 - r.x0) >= (r.y1 - r.y0)]

    # At each intersection, place crosswalks
    for ns in ns_roads:
        for ew in ew_roads:
            # Check if they overlap
            if ns.x1 < ew.x0 or ns.x0 > ew.x1:
                continue
            if ew.y1 < ns.y0 or ew.y0 > ns.y1:
                continue

            # Intersection center
            ix = (max(ns.x0, ew.x0) + min(ns.x1, ew.x1)) / 2
            iy = (max(ns.y0, ew.y0) + min(ns.y1, ew.y1)) / 2

            # Crosswalk across NS road (E-W stripes)
            stripe_w = 0.5
            stripe_gap = 0.5
            stripe_len = ns.x1 - ns.x0 - 1.0  # slightly narrower than road
            n_stripes = int(min(ns.width, ew.width) / (stripe_w + stripe_gap))

            # North crosswalk
            cy = ew.y0 - 0.5  # just before intersection
            for s in range(n_stripes):
                sx = ns.x0 + 0.5
                sy = cy - n_stripes * (stripe_w + stripe_gap) / 2 + s * (stripe_w + stripe_gap)
                v, f = make_quad(sx, sy, sx + stripe_len, sy + stripe_w, z)
                all_v.append(v); all_f.append(f + offset); offset += len(v)

            # South crosswalk
            cy = ew.y1 + 0.5
            for s in range(n_stripes):
                sx = ns.x0 + 0.5
                sy = cy + s * (stripe_w + stripe_gap)
                v, f = make_quad(sx, sy, sx + stripe_len, sy + stripe_w, z)
                all_v.append(v); all_f.append(f + offset); offset += len(v)

            # East crosswalk (N-S stripes across EW road)
            cx = ns.x1 + 0.5
            for s in range(n_stripes):
                sy = ew.y0 + 0.5
                sx = cx + s * (stripe_w + stripe_gap)
                v, f = make_quad(sx, sy, sx + stripe_w, sy + ew.width - 1.0, z)
                all_v.append(v); all_f.append(f + offset); offset += len(v)

            # West crosswalk
            cx = ns.x0 - n_stripes * (stripe_w + stripe_gap) - 0.5
            for s in range(n_stripes):
                sy = ew.y0 + 0.5
                sx = cx + s * (stripe_w + stripe_gap)
                v, f = make_quad(sx, sy, sx + stripe_w, sy + ew.width - 1.0, z)
                all_v.append(v); all_f.append(f + offset); offset += len(v)

    if not all_v:
        return np.zeros((0, 3), np.float32), np.zeros((0, 3), np.int32)
    return np.vstack(all_v), np.vstack(all_f)


def generate_center_lines(roads):
    """Generate dashed center line markings on roads."""
    all_v, all_f = [], []
    offset = 0
    z = 0.04
    dash_len = 3.0
    dash_gap = 3.0
    line_w = 0.15

    for r in roads:
        if (r.y1 - r.y0) > (r.x1 - r.x0):
            # NS road — center line along Y
            cx = (r.x0 + r.x1) / 2 - line_w / 2
            y = r.y0 + 2.0
            while y + dash_len < r.y1 - 2.0:
                v, f = make_quad(cx, y, cx + line_w, y + dash_len, z)
                all_v.append(v); all_f.append(f + offset); offset += len(v)
                y += dash_len + dash_gap
        else:
            # EW road — center line along X
            cy = (r.y0 + r.y1) / 2 - line_w / 2
            x = r.x0 + 2.0
            while x + dash_len < r.x1 - 2.0:
                v, f = make_quad(x, cy, x + dash_len, cy + line_w, z)
                all_v.append(v); all_f.append(f + offset); offset += len(v)
                x += dash_len + dash_gap

    if not all_v:
        return np.zeros((0, 3), np.float32), np.zeros((0, 3), np.int32)
    return np.vstack(all_v), np.vstack(all_f)


# ---------------------------------------------------------------------------
# Ground plane
# ---------------------------------------------------------------------------
def generate_ground(town_w, town_h, margin=50.0):
    """Big flat ground plane under everything."""
    v, f = make_quad(-margin, -margin, town_w + margin, town_h + margin,
                      z=-0.01)
    return v, f


# ---------------------------------------------------------------------------
# OBJ export
# ---------------------------------------------------------------------------
def save_obj(path, verts, faces, name="mesh"):
    """Save vertices + faces as OBJ."""
    with open(path, 'w') as f:
        f.write(f"# {name}\n")
        f.write(f"# {len(verts)} vertices, {len(faces)} faces\n")
        for v in verts:
            f.write(f"v {v[0]:.4f} {v[1]:.4f} {v[2]:.4f}\n")
        for face in faces:
            # OBJ faces are 1-indexed
            f.write(f"f {face[0]+1} {face[1]+1} {face[2]+1}\n")


