def make_quad(x0, y0, x1, y1, z=0.0):
    """Create a flat quad (2 triangles)."""
    verts = np.array([
        [x0, y0, z], [x1, y0, z], [x1, y1, z], [x0, y1, z]
    ], dtype=np.float32)
    faces = np.array([[0, 1, 2], [0, 2, 3]], dtype=np.int32)
    return verts, faces


def generate_road_meshes(roads):
    """Generate road surface quads (slightly above ground to avoid z-fighting)."""
    all_v, all_f = [], []
    offset = 0
    for r in roads:
        v, f = make_quad(r.x0, r.y0, r.x1, r.y1, z=0.02)
        all_v.append(v)
        all_f.append(f + offset)
        offset += len(v)
    return np.vstack(all_v), np.vstack(all_f)


def make_curb_strip(x0, y0, x1, y1, height, width, side='left'):
    """Create an extruded curb strip (box)."""
    # Determine orientation
    dx = x1 - x0
    dy = y1 - y0
    length = np.sqrt(dx * dx + dy * dy)

    if length < 0.1:
        return np.zeros((0, 3), np.float32), np.zeros((0, 3), np.int32)

    # 8 vertices: 4 bottom + 4 top
    verts = np.array([
        [x0, y0, 0], [x1, y1, 0],
        [x0 + width * (dy / length if side == 'left' else -dy / length),
         y0 + width * (-dx / length if side == 'left' else dx / length), 0],
        [x1 + width * (dy / length if side == 'left' else -dy / length),
         y1 + width * (-dx / length if side == 'left' else dx / length), 0],
        # Top
        [x0, y0, height], [x1, y1, height],
        [x0 + width * (dy / length if side == 'left' else -dy / length),
         y0 + width * (-dx / length if side == 'left' else dx / length), height],
        [x1 + width * (dy / length if side == 'left' else -dy / length),
         y1 + width * (-dx / length if side == 'left' else dx / length), height],
    ], dtype=np.float32)

    faces = np.array([
        # Bottom
        [0, 1, 3], [0, 3, 2],
        # Top
        [4, 7, 5], [4, 6, 7],
        # Sides
        [0, 4, 5], [0, 5, 1],
        [2, 3, 7], [2, 7, 6],
        [0, 2, 6], [0, 6, 4],
        [1, 5, 7], [1, 7, 3],
    ], dtype=np.int32)

    return verts, faces


def generate_curb_meshes(roads):
    """Generate curb strips along all road edges."""
    all_v, all_f = [], []
    offset = 0

    for r in roads:
        # For N-S roads (tall thin rectangles)
        if (r.y1 - r.y0) > (r.x1 - r.x0):
            # Left curb
            v, f = make_curb_strip(r.x0, r.y0, r.x0, r.y1,
                                    CURB_H, CURB_W, 'right')
            if len(v) > 0:
                all_v.append(v)
                all_f.append(f + offset)
                offset += len(v)
            # Right curb
            v, f = make_curb_strip(r.x1, r.y0, r.x1, r.y1,
                                    CURB_H, CURB_W, 'left')
            if len(v) > 0:
                all_v.append(v)
                all_f.append(f + offset)
                offset += len(v)
        else:
            # E-W roads
            # Bottom curb
            v, f = make_curb_strip(r.x0, r.y0, r.x1, r.y0,
                                    CURB_H, CURB_W, 'left')
            if len(v) > 0:
                all_v.append(v)
                all_f.append(f + offset)
                offset += len(v)
            # Top curb
            v, f = make_curb_strip(r.x0, r.y1, r.x1, r.y1,
                                    CURB_H, CURB_W, 'right')
            if len(v) > 0:
                all_v.append(v)
                all_f.append(f + offset)
                offset += len(v)

    if not all_v:
        return np.zeros((0, 3), np.float32), np.zeros((0, 3), np.int32)
    return np.vstack(all_v), np.vstack(all_f)


def generate_sidewalk_meshes(roads):
    """Generate sidewalk strips along road edges."""
    all_v, all_f = [], []
    offset = 0

    for r in roads:
        sw = SIDEWALK_W
        if (r.y1 - r.y0) > (r.x1 - r.x0):
            # N-S road — sidewalks on left and right
            # Left sidewalk
            v, f = make_quad(r.x0 - sw - CURB_W, r.y0,
                              r.x0 - CURB_W, r.y1, z=0.01)
            all_v.append(v); all_f.append(f + offset); offset += len(v)
            # Right sidewalk
            v, f = make_quad(r.x1 + CURB_W, r.y0,
                              r.x1 + CURB_W + sw, r.y1, z=0.01)
            all_v.append(v); all_f.append(f + offset); offset += len(v)
        else:
            # E-W road — sidewalks on top and bottom
            v, f = make_quad(r.x0, r.y0 - sw - CURB_W,
                              r.x1, r.y0 - CURB_W, z=0.01)
            all_v.append(v); all_f.append(f + offset); offset += len(v)
            v, f = make_quad(r.x0, r.y1 + CURB_W,
                              r.x1, r.y1 + CURB_W + sw, z=0.01)
            all_v.append(v); all_f.append(f + offset); offset += len(v)

    return np.vstack(all_v), np.vstack(all_f)


def _make_cylinder(x, y, z, radius, height, n=8):
    """Cylinder mesh: returns (verts, faces) lists."""
    angles = np.linspace(0, 2 * np.pi, n, endpoint=False)
    ca, sa = np.cos(angles), np.sin(angles)
    verts, faces = [], []
    # Bottom ring
    for i in range(n):
        verts.append([x + radius * ca[i], y + radius * sa[i], z])
    # Top ring
    for i in range(n):
        verts.append([x + radius * ca[i], y + radius * sa[i], z + height])
    # Side faces
    for i in range(n):
        j = (i + 1) % n
        faces.append([i, j, n + j])
        faces.append([i, n + j, n + i])
    # Bottom cap
    for i in range(1, n - 1):
        faces.append([0, i + 1, i])
    # Top cap
    for i in range(1, n - 1):
        faces.append([n, n + i, n + i + 1])
    return verts, faces


def _make_cone(x, y, z, radius, height, n=8):
    """Cone mesh: returns (verts, faces) lists."""
    angles = np.linspace(0, 2 * np.pi, n, endpoint=False)
    ca, sa = np.cos(angles), np.sin(angles)
    verts, faces = [], []
    # Base ring
    for i in range(n):
        verts.append([x + radius * ca[i], y + radius * sa[i], z])
    # Tip
    tip = n
    verts.append([x, y, z + height])
    # Side faces
    for i in range(n):
        j = (i + 1) % n
        faces.append([i, j, tip])
    # Base cap
    for i in range(1, n - 1):
        faces.append([0, i + 1, i])
    return verts, faces


def _make_sphere_crown(x, y, z, radius, n_lat=4, n_lon=8):
    """Half-sphere crown (dome): returns (verts, faces) lists."""
    verts, faces = [], []
    # Top pole
    verts.append([x, y, z + radius])
    # Latitude rings (from top down to equator)
    for i in range(1, n_lat + 1):
        phi = (np.pi / 2) * i / n_lat  # 0 to pi/2
        r = radius * np.sin(phi)
        zz = z + radius * np.cos(phi)
        for j in range(n_lon):
            theta = 2 * np.pi * j / n_lon
            verts.append([x + r * np.cos(theta), y + r * np.sin(theta), zz])
    # Top cap (connect pole to first ring)
    for j in range(n_lon):
        j2 = (j + 1) % n_lon
        faces.append([0, 1 + j2, 1 + j])
    # Middle rings
    for i in range(n_lat - 1):
        ring1 = 1 + i * n_lon
        ring2 = 1 + (i + 1) * n_lon
        for j in range(n_lon):
            j2 = (j + 1) % n_lon
            faces.append([ring1 + j, ring1 + j2, ring2 + j2])
            faces.append([ring1 + j, ring2 + j2, ring2 + j])
    # Bottom cap
    last_ring = 1 + (n_lat - 1) * n_lon
    bottom = len(verts)
    verts.append([x, y, z])
    for j in range(n_lon):
        j2 = (j + 1) % n_lon
        faces.append([last_ring + j, last_ring + j2, bottom])
    return verts, faces


def make_tree(x, y, z=0.0, seed_val=0):
    """Generate a procedural tree with varied shapes.

    3 tree types:
      0 = Round deciduous (sphere crown)
      1 = Conical pine (2-3 stacked cones)
      2 = Multi-layer deciduous (2 sphere layers)
    """
    rng = np.random.default_rng(seed_val)
    tree_type = seed_val % 3
    n = TREE_SIDES

    # Random variation
    scale = rng.uniform(0.7, 1.3)
    trunk_h = TREE_TRUNK_H * scale * rng.uniform(0.8, 1.2)
    trunk_r = TREE_TRUNK_R * rng.uniform(0.7, 1.1)

    all_verts = []
    all_faces = []

    # Trunk
    tv, tf = _make_cylinder(x, y, z, trunk_r, trunk_h, n)
    all_verts.extend(tv)
    all_faces.extend(tf)
    offset = len(tv)

    crown_base = z + trunk_h * 0.85  # crown overlaps trunk slightly

    if tree_type == 0:
        # Round deciduous — sphere dome
        crown_r = TREE_CROWN_R * scale * rng.uniform(0.8, 1.2)
        cv, cf = _make_sphere_crown(x, y, crown_base, crown_r, n_lat=3, n_lon=n)
        cf = [[f[0] + offset, f[1] + offset, f[2] + offset] for f in cf]
        all_verts.extend(cv)
        all_faces.extend(cf)

    elif tree_type == 1:
        # Conical pine — 2-3 stacked cones getting smaller
        n_layers = rng.choice([2, 3])
        for layer in range(n_layers):
            frac = layer / n_layers
            cone_r = TREE_CROWN_R * scale * (1.0 - frac * 0.35) * rng.uniform(0.85, 1.15)
            cone_h = TREE_CROWN_H * scale * 0.6 * rng.uniform(0.85, 1.15)
            cone_z = crown_base + layer * cone_h * 0.55
            cv, cf = _make_cone(x, y, cone_z, cone_r, cone_h, n)
            cf = [[f[0] + offset, f[1] + offset, f[2] + offset] for f in cf]
            all_verts.extend(cv)
            all_faces.extend(cf)
            offset += len(cv)

    else:
        # Multi-layer deciduous — 2 overlapping domes
        for layer in range(2):
            off_x = rng.uniform(-0.5, 0.5) * scale
            off_y = rng.uniform(-0.5, 0.5) * scale
            crown_r = TREE_CROWN_R * scale * rng.uniform(0.7, 1.1)
            layer_z = crown_base + layer * crown_r * 0.4
            cv, cf = _make_sphere_crown(
                x + off_x, y + off_y, layer_z, crown_r, n_lat=3, n_lon=n)
            cf = [[f[0] + offset, f[1] + offset, f[2] + offset] for f in cf]
            all_verts.extend(cv)
            all_faces.extend(cf)
            offset += len(cv)

    return (np.array(all_verts, dtype=np.float32),
            np.array(all_faces, dtype=np.int32))


def generate_trees(roads, building_footprints, town_w, town_h, seed=42):
    """Place trees along sidewalks, avoiding building footprints."""
    rng = np.random.default_rng(seed + 100)
    all_v, all_f = [], []
    offset = 0
    tree_id = 0

    # Collect tree candidate positions along roads
    positions = []

    for r in roads:
        sw = SIDEWALK_W
        if (r.y1 - r.y0) > (r.x1 - r.x0):
            # N-S road — trees along sidewalks
            ys = np.arange(r.y0 + 5, r.y1 - 5, TREE_SPACING)
            for y in ys:
                # Left sidewalk
                positions.append((r.x0 - CURB_W - sw / 2, y))
                # Right sidewalk
                positions.append((r.x1 + CURB_W + sw / 2, y))
        else:
            # E-W road
            xs = np.arange(r.x0 + 5, r.x1 - 5, TREE_SPACING)
            for x in xs:
                positions.append((x, r.y0 - CURB_W - sw / 2))
                positions.append((x, r.y1 + CURB_W + sw / 2))

    # Filter out trees that overlap building footprints
    fps = np.array(building_footprints) if building_footprints else np.zeros((0, 4))

    for px, py in positions:
        # Check overlap with any building footprint (with margin)
        if len(fps) > 0:
            inside = ((px > fps[:, 0] - 2) & (px < fps[:, 2] + 2) &
                      (py > fps[:, 1] - 2) & (py < fps[:, 3] + 2))
            if inside.any():
                continue

        # Bounds check
        if px < 2 or px > town_w - 2 or py < 2 or py > town_h - 2:
            continue

        v, f = make_tree(px, py, seed_val=tree_id * 7 + seed)
        all_v.append(v)
        all_f.append(f + offset)
        offset += len(v)
        tree_id += 1

    if not all_v:
        return np.zeros((0, 3), np.float32), np.zeros((0, 3), np.int32)
    return np.vstack(all_v), np.vstack(all_f)


